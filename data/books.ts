export function getBooksByCentury(): Map<string, YearBook[]> {
  const map = new Map<string, YearBook[]>()
  for (const book of BOOKS) {
    const century = book.year < 1000
      ? '~999'
      : `${Math.floor(book.year / 100) * 100}년대`
    if (!map.has(century)) map.set(century, [])
    map.get(century)!.push(book)
  }
  return map
}
